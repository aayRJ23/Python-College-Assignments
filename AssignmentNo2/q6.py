concatenate_names = lambda friend1, friend2, friend3: friend1 + ' ' + friend2 + ' ' + friend3

# Example usage:
friend1 = input("Enter the name of your first friend: ")
friend2 = input("Enter the name of your second friend: ")
friend3 = input("Enter the name of your third friend: ")

result = concatenate_names(friend1, friend2, friend3)
print("Concatenated names of your friends:", result)
